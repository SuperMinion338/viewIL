const BLOCKED_WORDS = [
  // Core words from spec
  "זונה", "זונות",
  "שרמוטה", "שרמוטות",
  "סקס",
  "זיון", "זיונים",
  "מזדיין", "מזדיינים",
  "מזדיינת", "מזדיינות",
  "כוס", "כוסות",
  "כוסאמא", "כוסאמק", "כוסעמק", "כוס אמא",
  "זין", "זינים",
  "לזיין",
  "מציצה", "מציצות",
  "זבל",
  "חרא",
  "בן זונה",
  "בת זונה",
  "נבלה", "נבלות",
  // Similar variations
  "לדפוק", "דפוק", "דפוקה", "דפוקות",
  "מדופק", "מדופקת", "מדופקים", "מדופקות",
  "זיינתי", "זיינו", "תזדיין", "תזדייני",
  "כוסמק",
];

export function containsBlockedWords(text: string): boolean {
  if (!text) return false;
  return BLOCKED_WORDS.some((word) => text.includes(word));
}
